-- 2.2 Create Functions

-- Computed Columns based on a function

GO
CREATE FUNCTION dbo.EventStatus(@EVTID INT)
RETURNS  VARCHAR (30)
AS
   BEGIN
      DECLARE @STAT VARCHAR (30);
      DECLARE @DATE DATE = CONVERT(CHAR(20), GETDATE(), 1);
      DECLARE @S_date date = (select StartDate from dbo.Event
                              where EVENTID = @EVTID);
      declare @E_date date = (select EndDate from dbo.Event
                              where EVENTID = @EVTID);
      if @S_date > @DATE
       SET @STAT = 'Not started Yet';
      ELSE if  @S_date <= @DATE  AND @E_date >= @DATE 
       SET @STAT = 'On-going';
      else 
      begin
       SET @STAT = 'Closed';
      end 
      RETURN @STAT;
END;

GO
ALTER TABLE dbo.Event
ADD status AS (dbo.EventStatus(EVENTID));


-- 2.3 Create Tables Check
-- Table-level CHECK Constraints based on a function
GO
CREATE FUNCTION dbo.checkEvent(@VID int)
returns INT
AS
BEGIN
      DECLARE @output INT = 0;
      DECLARE @countEvent INT = 0;
      DECLARE @countconflict INT = 0;
     
     SELECT @countconflict = COUNT(ver.EVENTID) FROM dbo.VolunteerEventReferral ver
      RIGHT JOIN dbo.EVENT  e1
      ON ver.EVENTID = e1.EVENTID
     INNER join dbo.EVENT  e2
     ON e2.StartDate > e1.StartDate AND e2.StartDate < e1.EndDate -- start before e1 is finished
     WHERE ver.VOLUNTEERSID = @VID 
     
     IF @countconflict >1
     SET @output =1;
     
      SELECT @countEvent= COUNT(ver.EVENTID) FROM dbo.VolunteerEventReferral ver 
      LEFT JOIN dbo.VOLUNTEERS v 
      ON ver.VOLUNTEERSID = v.VOLUNTEERSID 
      RIGHT JOIN dbo.EVENT  e
      ON ver.EVENTID = e.EVENTID
      WHERE v.VOLUNTEERSID  = @VID AND year(StartDate) = year(GETDATE())
     
      IF @countEvent > 5 
      SET @output =1;
      
    
RETURN @output;
END;

GO
ALTER TABLE  dbo.VolunteerEventReferral ADD CONSTRAINT eventcheck CHECK
(dbo.checkEvent(VOLUNTEERSID) = 0) ;


-- 2.4 Create views


GO
CREATE OR ALTER VIEW USER_EXPENDITURE AS
SELECT U.USERID, U.USERFNAME, U.USERLNAME, SUM(AMOUNT) AS TOTAL_AMOUNT FROM USERS U
LEFT JOIN EXPENDITURES E
ON U.USERID = E.USERID
GROUP BY U.USERID, U.USERFNAME, U.USERLNAME;


GO
CREATE OR ALTER VIEW DONORS_DONATIONS AS
SELECT D.DONORID, D.DONFNAME, D.DONLNAME, DS.DONATIONID, DS.NAME, DS.CONTRIBUTION  FROM DONORS D
LEFT JOIN DONATIONS DS
ON D.DONORID = DS.DONORID;


